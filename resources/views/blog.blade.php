            <h2>
                <span style="font-weight: 400;">「拍馬屁」的用法</span>
            </h2>
            <p>
                <span style="font-weight: 400;">瞭解「拍馬屁」的藝術前，我們先來學學「拍馬屁」的英文要怎麼說吧！</span>
            </p>
            <h3>
                <b>brown-noser/brown-nose


                    <br />
                </b>
            </h3>
            <p>
                <img class="aligncenter wp-image-60906 size-full" src="https://tw.blog.voicetube.com/wp-content/uploads/2017/10/cde17de6-nose.jpg"
                    alt="" width="95%" />
            </p>
            <p>
                <span style="font-weight: 400;">這個字的解釋其實是比較具有貶意的，它是指馬屁精的行為，就像是把鼻子放在想拍馬屁對象的屁股位置，因此鼻子才會沾到穢物而變成咖啡色。</span>
            </p>
            <blockquote>
                <p>
                    <span style="font-weight: 400;">The</span>
                    <b> brown-noser</b>
                    <span style="font-weight: 400;"> bought a bottle of wine for his boss.


                        <br />
                    </span>
                    <span style="font-weight: 400;">那個馬屁精買了一瓶酒給他的老闆。</span>
                </p>
            </blockquote>
            <h3>
                <b>apple polisher</b>
            </h3>
            <p>
                <img class="aligncenter wp-image-60903 size-full" src="https://tw.blog.voicetube.com/wp-content/uploads/2017/10/c3d1e6f8-applepolisher.jpg"
                    alt="" width="95%" />
            </p>
            <p>
                <span style="font-weight: 400;">把蘋果擦亮和拍馬屁有什麼關係呢？它的由來是因為美國的學生會藉由送蘋果讓老師開心，希望老師可以多多關照自己。</span>
            </p>
            <blockquote>
                <p>
                    <span style="font-weight: 400;">My ex-coworker is a typical </span>
                    <b>apple-polisher</b>
                    <span style="font-weight: 400;"> because she agrees pretty much everything the boss says.


                        <br />
                    </span>
                    <span style="font-weight: 400;">我的前同事是個典型的馬屁精，因為她總是贊同老闆說的每一句話。</span>
                </p>
            </blockquote>
            <h3>
                <b>the boss&#8217;s pet</b>
            </h3>
            <p>
                <img class="aligncenter wp-image-60907 size-full" src="https://tw.blog.voicetube.com/wp-content/uploads/2017/10/d51555e0-pet.jpg"
                    alt="" width="95%" />
            </p>
            <p>
                <span style="font-weight: 400;">當一個人表現得對老闆言聽計從，那他的行為就像是老闆養的寵物一樣。</span>
            </p>
            <blockquote>
                <p>
                    <span style="font-weight: 400;">If you act like a </span>
                    <b>boss&#8217;s pet</b>
                    <span style="font-weight: 400;">, it may affect your friendship with your colleagues.


                        <br />
                    </span>
                    <span style="font-weight: 400;">如果你表現得像是個馬屁精，你有可能失去與同事的友誼。</span>
                </p>
            </blockquote>
