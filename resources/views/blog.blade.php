            <h2>
                <span style="font-weight: 400;">「拍馬屁」藝術：不著痕跡的技巧</span>
            </h2>
            <p>
                <span style="font-weight: 400;">「拍馬屁」是一門藝術，它的重點在於如何抓對時機說對話，因為這樣就可以讓你在同樣的情況下，比別人更具優勢。如果你還對「拍馬屁」有壞印象，趕快看這支影片，你會了解這其實是件有益的事！</span>
            </p>
            <p style="text-align: center;">
                <span style="font-weight: 400;">
                    <a href="https://tw.voicetube.com/videos/52221?mtc=blog_sunny_60900_20171103" target="_blank" rel="noopener">阿諛諂媚，其實很重要？ (The Importance of Flattery)</a>
                    <br />
                    <a href="https://tw.voicetube.com/videos/52221?mtc=blog_sunny_60900_20171103" target="_blank" rel="noopener">
                        <img class="aligncenter wp-image-60901 size-full" src="https://tw.blog.voicetube.com/wp-content/uploads/2017/10/813039ae-blog-beapro1.jpg"
                            alt="" width="95%" />
                    </a>
                </span>
            </p>
            <h3>
                <span style="font-weight: 400;">1. 把拍馬屁包裝成尋求意見</span>
            </h3>
            <p>
                <span style="font-weight: 400;">當對方主導一個成功的策略時，你可以這樣說：</span>
            </p>
            <blockquote>
                <p>
                    <span style="font-weight: 400;">“The marketing campaign was a great success. How were you able to pull off the strategy so successfully?”


                        <br />
                    </span>
                    <span style="font-weight: 400;">「這次的行銷活動超成功。你是怎麼成功執行這個策略的？」 </span>
                </p>
            </blockquote>
            <p>
                <span style="font-weight: 400;">當對方所提的廣告達到不錯成效時，你可以這樣說：</span>
            </p>
            <blockquote>
                <p>
                    <span style="font-weight: 400;">“We received a lot of positive feedback about our new commercial recently. How were you able to come
                        up with such a creative idea?”


                        <br />
                    </span>
                    <span style="font-weight: 400;">「我們最近收到很多對新廣告的正向回饋。你是怎麼想出這麼有創意的點子的？」</span>
                </p>
            </blockquote>
            <h3>
                <span style="font-weight: 400;">2. 暗示你拍馬屁的對象：我要拍你馬屁了！</span>
            </h3>
            <p>
                <span style="font-weight: 400;">當對方換了一個新髮型時，你可以這樣說：</span>
            </p>
            <blockquote>
                <p>
                    <span style="font-weight: 400;">“I don&#8217;t want to embarrass you, but you look great with that new hairstyle.”


                        <br />
                    </span>
                    <span style="font-weight: 400;">「我知道這樣說會讓你尷尬，但你的新髮型真的很好看。」</span>
                </p>
            </blockquote>
            <p>
                <span style="font-weight: 400;">當對方有一些特殊表現時，你可以這樣說：</span>
            </p>
            <blockquote>
                <p>
                    <span style="font-weight: 400;">“You are going to hate me for saying this, but you really are a great asset to our company.”  </span>
                    <span style="font-weight: 400;">「我知道你可能不會想聽到這個，但你真的是本公司最棒的資產。」</span>
                </p>
                <p>
                    <span style="font-weight: 400;">“I know you won&#8217;t want me to say this, but your outstanding performance has always been a great
                        example for us to follow.”


                        <br />
                    </span>
                    <span style="font-weight: 400;">「我知道你不希望我這麼說，但是你的優秀表現一直是我們仿效的模範。」</span>
                </p>
            </blockquote>
            <h3>
                <span style="font-weight: 400;">3. 對奉承對象周遭的朋友讚美他</span>
            </h3>
            <p>
                <span style="font-weight: 400;">有些人可能會不喜歡別人直接讚美他，但如果你跟他的朋友讚美他，當這個讚美傳到他耳裡，讚美就顯得更誠心。</span>
            </p>
            <blockquote>
                <p>
                    <span style="font-weight: 400;">“I really admire his decision-making and execution abilities.”


                        <br />
                    </span>
                    <span style="font-weight: 400;">「我非常欽佩他的決策和執行能力。」</span>
                </p>
            </blockquote>
            <h3>
                <span style="font-weight: 400;">4. 不要馬上說 “Yes” 或 “I agree” </span>
            </h3>
            <p>
                <span style="font-weight: 400;">當你想奉承的對象提出任何意見或想法時，如果你想都不想的在第一時間就附和或者稱讚他，對方很容易察覺你只是想對他拍馬屁。因此在這種情況下，你應該要先提出自己的建議 (不是用詞尖銳的建議)，然後再附和或稱讚對方的想法，這樣就顯得真誠多了！</span>
            </p>
            <blockquote>
                <p>
                    <span style="font-weight: 400;">Supervisor：What do you think of the project I presented?


                        <br />
                    </span>
                    <span style="font-weight: 400;">Subordinate：I think it’s a good idea, but it won’t work unless you have accurate data. What sources did
                        you use?


                        <br />
                    </span>
                    <span style="font-weight: 400;">Supervisor：It’s all World Bank and United Nations data—it’s all super accurate.


                        <br />
                    </span>
                    <span style="font-weight: 400;">Subordinate：Wow! That’s great! I’m excited to see how it turns out.</span>
                </p>
                <p>
                    <span style="font-weight: 400;">主管：你覺得我剛剛報告的專案計畫如何？


                        <br />
                    </span>
                    <span style="font-weight: 400;">部屬：我覺得是個不錯的想法，但是必須要有精確的數據才能執行。你採用的資料來源是什麼？


                        <br />
                    </span>
                    <span style="font-weight: 400;">主管：全部的數據都是世界銀行和聯合國的資料，都是非常精確的數據。


                        <br />
                    </span>
                    <span style="font-weight: 400;">部屬：哇！這樣超棒的！我很期待看到成果。</span>
                </p>
            </blockquote>
            <p>看完這篇文章，你是不是對拍馬屁這件事改觀了呢？拍馬屁其實就是懂得在對的時間說好聽話，這不僅讓聽的人開心，也讓說的人得到想要獲得的結果，簡直就是雙贏的局面啊！</p>
